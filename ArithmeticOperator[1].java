public class ArithmeticOperator {
    public static void main(String[] args) {
        int a=20,b=5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println("yash"+"Sai");
        System.out.println(10+"yash");
        System.out.println(10+"yash"+20+30);
        System.out.println(10+20+"yash"+20+30);
        System.out.println(10+"yash"+3*10/2+"Sai");
        /*System.out.println(10+"yash"+3*10/2-1+"Sai");
        - is not possible for string*/
    }    
}